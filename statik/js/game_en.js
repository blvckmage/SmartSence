// Общие элементы для всех страниц
const nextButton = document.getElementById('nextButton');
const backgroundImage = document.querySelector('.background-image');

function updateDialogue() {
    if (currentDialogueIndex < dialogueArray.length) {
        const currentDialogue = dialogueArray[currentDialogueIndex];
        dialogueElement.innerHTML = currentDialogue.text;
        backgroundImage.style.backgroundImage = currentDialogue.background;
        currentDialogueIndex++;
    } else {
        nextButton.style.display = 'none';
    }
}

nextButton.addEventListener('click', updateDialogue);

if (dialogueElement) {
    updateDialogue();
}

